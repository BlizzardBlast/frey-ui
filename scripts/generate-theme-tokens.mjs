function printHelp() {
  process.stdout.write(`Usage:
  pnpm theme:tokens -- --primary <hex> [--neutral <hex>] [--success <hex>] [--warning <hex>] [--error <hex>] [--info <hex>]
`);
}

function main() {
  try {
    const parsed = parseArguments(process.argv.slice(2));

    if (parsed.help) {
      printHelp();
      return;
    }

    if (!parsed.primary) {
      throw new Error('Missing required --primary <hex> argument.');
    }

    const colors = {
      primary: normalizeHex(parsed.primary, '--primary'),
      neutral: normalizeHex(
        parsed.neutral ?? DEFAULT_COLORS.neutral,
        '--neutral'
      ),
      success: normalizeHex(
        parsed.success ?? DEFAULT_COLORS.success,
        '--success'
      ),
      warning: normalizeHex(
        parsed.warning ?? DEFAULT_COLORS.warning,
        '--warning'
      ),
      error: normalizeHex(parsed.error ?? DEFAULT_COLORS.error, '--error'),
      info: normalizeHex(parsed.info ?? DEFAULT_COLORS.info, '--info')
    };

    process.stdout.write(buildCss(colors));
  } catch (error) {
    writeError(error instanceof Error ? error.message : 'Unknown error.');
    process.exitCode = 1;
  }
}

main();
