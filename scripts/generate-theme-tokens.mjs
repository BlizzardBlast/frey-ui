const DEFAULT_COLORS = {
  neutral: '#6b7280',
  success: '#16a34a',
  warning: '#d97706',
  error: '#dc2626',
  info: '#2563eb'
};

const SUPPORTED_FLAGS = new Set([
  '--primary',
  '--neutral',
  '--success',
  '--warning',
  '--error',
  '--info',
  '--help'
]);

function writeError(message) {
  process.stderr.write(`${message}\n`);
}

function normalizeHex(value, flagName) {
  if (typeof value !== 'string') {
    throw new Error(`Missing color value for ${flagName}.`);
  }

  const trimmed = value.trim();
  const match = /^#([0-9a-fA-F]{3}|[0-9a-fA-F]{6})$/.exec(trimmed);

  if (!match) {
    throw new Error(`Invalid hex color for ${flagName}: "${value}".`);
  }

  const raw = match[1].toLowerCase();

  if (raw.length === 3) {
    return `#${raw
      .split('')
      .map((character) => `${character}${character}`)
      .join('')}`;
  }

  return `#${raw}`;
}

function hexToRgb(hex) {
  const value = hex.slice(1);
  return {
    red: Number.parseInt(value.slice(0, 2), 16),
    green: Number.parseInt(value.slice(2, 4), 16),
    blue: Number.parseInt(value.slice(4, 6), 16)
  };
}

function rgbToHex({ red, green, blue }) {
  const toHex = (channel) => {
    return Math.round(Math.max(0, Math.min(255, channel)))
      .toString(16)
      .padStart(2, '0');
  };

  return `#${toHex(red)}${toHex(green)}${toHex(blue)}`;
}

function mix(baseHex, targetHex, targetWeight) {
  const clampedWeight = Math.max(0, Math.min(1, targetWeight));
  const base = hexToRgb(baseHex);
  const target = hexToRgb(targetHex);

  return rgbToHex({
    red: base.red * (1 - clampedWeight) + target.red * clampedWeight,
    green: base.green * (1 - clampedWeight) + target.green * clampedWeight,
    blue: base.blue * (1 - clampedWeight) + target.blue * clampedWeight
  });
}

function lighten(hex, amount) {
  return mix(hex, '#ffffff', amount);
}

function darken(hex, amount) {
  return mix(hex, '#000000', amount);
}

function parseArguments(argv) {
  const parsed = {};

  for (let index = 0; index < argv.length; index += 1) {
    const flag = argv[index];

    if (!SUPPORTED_FLAGS.has(flag)) {
      throw new Error(`Unknown flag "${flag}". Run with --help for usage.`);
    }

    if (flag === '--help') {
      parsed.help = true;
      continue;
    }

    const value = argv[index + 1];

    if (!value || value.startsWith('--')) {
      throw new Error(`Missing value for ${flag}.`);
    }

    parsed[flag.replace(/^--/, '')] = value;
    index += 1;
  }

  return parsed;
}

function statusTokens(color) {
  return {
    base: color,
    lightBackground: lighten(color, 0.9),
    lightBorder: lighten(color, 0.65),
    darkForeground: lighten(color, 0.2),
    darkBackground: darken(color, 0.78),
    darkBorder: darken(color, 0.5),
    highContrastLight: darken(color, 0.22),
    highContrastDark: lighten(color, 0.25)
  };
}

